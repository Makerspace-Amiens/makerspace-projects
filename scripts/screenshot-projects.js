#!/usr/bin/env node
// Screenshots each project's page to use as a card thumbnail, for projects that
// don't have a custom GitHub social-preview image (set by fetch-projects.js).
// Covers both _data/projects.yml (auto-fetched) and _data/manual_projects.yml
// (hand-added entries, e.g. Thangs links) since manual entries have no GitHub
// social preview to fall back on.
// Run via `node scripts/screenshot-projects.js` after fetch-projects.js.

const fs = require("fs");
const path = require("path");
const yaml = require("js-yaml");
const { chromium } = require("playwright");

const SCREENSHOT_DIR = path.join(__dirname, "..", "assets", "screenshots");

const TARGETS = [
  {
    path: path.join(__dirname, "..", "_data", "projects.yml"),
    preserveHeader: false,
  },
  {
    path: path.join(__dirname, "..", "_data", "manual_projects.yml"),
    preserveHeader: true,
  },
];

function slugify(value) {
  return value
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function screenshotFilename(project) {
  if (project.org && project.name) {
    return `${project.org}__${project.name}.png`.toLowerCase();
  }
  return `${slugify(project.org || "manuel")}__${slugify(project.display_name)}.png`;
}

// _data/manual_projects.yml starts with explanatory `#` comments that aren't
// part of the data — keep them verbatim instead of letting yaml.dump drop them.
function splitHeader(raw) {
  const lines = raw.split("\n");
  let i = 0;
  while (i < lines.length && (lines[i].trim() === "" || lines[i].trim().startsWith("#"))) {
    i += 1;
  }
  return { header: lines.slice(0, i).join("\n"), rest: lines.slice(i).join("\n") };
}

async function processFile(page, { path: dataPath, preserveHeader }) {
  const raw = fs.readFileSync(dataPath, "utf8");
  const { header, rest } = preserveHeader ? splitHeader(raw) : { header: "", rest: raw };
  const projects = yaml.load(rest) || [];

  const force = process.env.FORCE_SCREENSHOTS === "true";
  let changed = false;

  for (const project of projects) {
    const target = project.pages_url || project.repo_url;
    if (project.image_url || !target) continue;

    const filename = screenshotFilename(project);
    const outputPath = path.join(SCREENSHOT_DIR, filename);

    if (!force && fs.existsSync(outputPath)) {
      project.image_url = `/assets/screenshots/${filename}`;
      changed = true;
      continue;
    }

    try {
      await page.goto(target, { waitUntil: "load", timeout: 30000 });
      await page.waitForTimeout(8000);
      await page.screenshot({ path: outputPath });
      project.image_url = `/assets/screenshots/${filename}`;
      changed = true;
      console.log(`Screenshotted ${project.display_name || project.name}`);
    } catch (err) {
      console.warn(`Skipping ${project.display_name || project.name}: ${err.message}`);
    }
  }

  if (!changed) return;

  const body = yaml.dump(projects, { lineWidth: 120 });
  const output = preserveHeader
    ? `${header}\n${body}`
    : `# Auto-generated by scripts/fetch-projects.js — do not edit by hand.\n${body}`;
  fs.writeFileSync(dataPath, output);
  console.log(`Updated ${dataPath}`);
}

async function main() {
  fs.mkdirSync(SCREENSHOT_DIR, { recursive: true });

  const browser = await chromium.launch();
  const page = await browser.newPage({
    viewport: { width: 1280, height: 800 },
    userAgent:
      "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/124.0.0.0 Safari/537.36",
  });

  for (const target of TARGETS) {
    await processFile(page, target);
  }

  await browser.close();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
