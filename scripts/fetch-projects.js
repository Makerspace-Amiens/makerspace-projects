#!/usr/bin/env node
// Fetches all public repos from the configured GitHub orgs and writes _data/projects.yml
// for the Jekyll site to render as cards. Run via `node scripts/fetch-projects.js`.

const fs = require("fs");
const path = require("path");
const yaml = require("js-yaml");

const CONFIG_PATH = path.join(__dirname, "..", "_config.yml");
const OUTPUT_PATH = path.join(__dirname, "..", "_data", "projects.yml");
const TOKEN = process.env.GITHUB_TOKEN;

const headers = {
  "Accept": "application/vnd.github+json",
  "User-Agent": "makerspace-projects-fetch-script",
  ...(TOKEN ? { Authorization: `Bearer ${TOKEN}` } : {}),
};

// GitHub renders a custom uploaded social-preview image from
// repository-images.githubusercontent.com. If none was uploaded, it falls back
// to an auto-generated card served from opengraph.githubassets.com — we only
// want the former, the latter isn't a useful project thumbnail.
async function getCustomSocialPreview(org, repoName) {
  const res = await fetch(`https://github.com/${org}/${repoName}`, {
    headers: { "User-Agent": "makerspace-projects-fetch-script" },
  });
  if (!res.ok) return null;
  const html = await res.text();
  const match = html.match(/<meta property="og:image" content="([^"]+)"/);
  if (!match) return null;
  const ogImage = match[1];
  return ogImage.includes("repository-images.githubusercontent.com") ? ogImage : null;
}

async function fetchAllRepos(org) {
  const repos = [];
  let page = 1;
  while (true) {
    const res = await fetch(
      `https://api.github.com/orgs/${org}/repos?per_page=100&page=${page}`,
      { headers }
    );
    if (!res.ok) {
      throw new Error(`GitHub API error for org ${org}: ${res.status} ${res.statusText}`);
    }
    const batch = await res.json();
    repos.push(...batch);
    if (batch.length < 100) break;
    page += 1;
  }
  return repos;
}

// "Puzzle-Bot-Groupe01" -> { group: "01", display: "Puzzle Bot - Groupe 01" }
function parseRepoName(name) {
  const groupMatch = name.match(/^(.+)-Groupe(\d+)$/i);
  if (!groupMatch) {
    return { group: null, display: name.replace(/-/g, " ") };
  }
  const [, project, group] = groupMatch;
  return {
    group,
    display: `${project.replace(/-/g, " ")} - Groupe ${group}`,
  };
}

async function main() {
  const config = yaml.load(fs.readFileSync(CONFIG_PATH, "utf8"));
  const orgs = config.organizations || [];

  const projects = [];

  for (const org of orgs) {
    const year = org.match(/(\d{4}-\d{2})$/)?.[1] || org;
    const repos = await fetchAllRepos(org);

    for (const repo of repos) {
      // Skip the org's own GitHub Pages site repo, it's not a student project.
      if (repo.name.toLowerCase() === `${org.toLowerCase()}.github.io`) continue;
      if (repo.archived) continue;

      const { group, display } = parseRepoName(repo.name);
      const pagesUrl = repo.has_pages
        ? `https://${org.toLowerCase()}.github.io/${repo.name}/`
        : null;
      const socialPreview = await getCustomSocialPreview(org, repo.name);

      projects.push({
        org,
        year,
        name: repo.name,
        group,
        display_name: display,
        description: repo.description || null,
        repo_url: repo.html_url,
        pages_url: pagesUrl,
        // Filled in by scripts/screenshot-projects.js when no custom preview exists.
        image_url: socialPreview,
        topics: repo.topics || [],
        language: repo.language || null,
        updated_at: repo.updated_at,
      });
    }
  }

  projects.sort((a, b) => a.year.localeCompare(b.year) || a.name.localeCompare(b.name));

  const header = "# Auto-generated by scripts/fetch-projects.js — do not edit by hand.\n";
  fs.writeFileSync(OUTPUT_PATH, header + yaml.dump(projects, { lineWidth: 120 }));
  console.log(`Wrote ${projects.length} projects to ${OUTPUT_PATH}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
